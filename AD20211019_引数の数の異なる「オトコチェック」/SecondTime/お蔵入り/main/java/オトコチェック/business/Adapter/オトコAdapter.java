package オトコチェック.business.Adapter;

public class オトコAdapter {
    private 比較対象殿方 my白馬の王子;

    public オトコAdapter(比較対象殿方 my白馬の王子){
        this.my白馬の王子 = my白馬の王子;
    }

    public Boolean is非該当(){
        return my白馬の王子.is非該当();
    }

    public String 非該当箇所(){
        return my白馬の王子.非該当箇所();
    }

    public String お名前(){
        return my白馬の王子.お名前();
    }

    public int 身長(){
        return my白馬の王子.身長();
    }

    public int 体重(){
        return my白馬の王子.体重();
    }

    public int 年齢(){
        return my白馬の王子.年齢();
    }
}